function openCreditos() {
  modalWrapperCreditos.style.visibility = "visible";
  modalWrapperCreditos.style.opacity = 1;
  modalCreditos.style.opacity = 1;
}

function cierraCreditos() {
  modalWrapperCreditos.style.visibility = "hidden";
  modalWrapperCreditos.style.opacity = 0;
  modalCreditos.style.opacity = 0;
}